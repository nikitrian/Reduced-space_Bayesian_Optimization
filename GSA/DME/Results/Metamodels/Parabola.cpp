double LegendreShiftPoly(double _xInp, int a)
{
	std::vector<std::vector<int>> coef = 
	{
		{2,-1},
		{6,-6,1},
		{20,-30,12,-1},
		{70,-140,90,-20,1},
		{252,-630,560,-210,30,-1},
		{924,-2772,3150,-1680,420,-42,1},
		{3432,-12012,16632,-11550,4200,-756,56,-1},
		{12870,-51480,84084,-72072,34650,-9240,1260,-72,1},
		{48620,-218790,411840,-420420,252252,-90090,18480,-1980,90,-1},
		{184756,-923780,1969110,-2333760,1681680,-756756,210210,-34320,2970,-110,1}
	};
	double temp = (double)coef[a - 1][0];
	for (int b = 0; b < a; b++)
		temp = temp * _xInp + (double)coef[a - 1][b + 1];
	return temp * sqrt((double)(2 * a + 1));
}

double combination(int n, int k)
{
	double num = 1.0, den = 1.0;
	int limit = ((n - k) < k) ? (n - k) : k;
	for (int i = 0; i < limit; i++)
	{
		num *= n - i;
		den *= i + 1;
	}
	return (num / den);
}

double LegendreShiftPoly_v2(double _xInp, int a)
{
	double temp = 0.0;
	for (int b = 0; b <= a; b++)
		temp += combination(a, b) * combination(a + b, b) * pow(-1.0 * _xInp, (double)b);
	return temp * pow(-1.0, (double)a) * sqrt((double)(2 * a + 1.0));
}

